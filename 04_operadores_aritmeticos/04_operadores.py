#Operadores Aritmeticos
# SUMA + RESTA - MULTIPLCAR * DIVIDIR /

valor1= 4
valor2= 3

sumar= valor1 + valor2
restar= valor1 - valor2
multiplicar= valor1 * valor2
dividir= valor1 / valor2

#Muestra resultados 
#SUMAR en el Print 
print (valor1 + valor2)

#Otra forma mostrar los valores 
print (f"El resultado de la Suma es :  {sumar}")
print (f"El resultado de la Resta es :  {restar}")
print (f"El resultado de la Multiplicacion es : {multiplicar}")
print (f"El resultado de la Division es : {dividir}")

 
#operador de asignacion EJ += o -=
valor = 1 
valor += valor  #incrementa el valor original en 1 
print (valor) 

valor_resta = 1 
valor_resta -= valor_resta  #decrementa el valor original en 1 
print (valor_resta) 


#Incrementa - Decrementa

valor_suma = 10
valor_suma = valor_suma + 1 
print (valor_suma) # Resultado = 11

valor_restar = 10
valor_restar = valor_restar - 1 
print (valor_restar) # Resultado = 9